package com.rl.common.objects;

/**
 *
 * @author efabuba
 */
public class MonitoringConfig {
    private String ip;
    private long port;
    private String name;

    public MonitoringConfig(String ip, long port, String name) {
        this.ip = ip;
        this.port = port;
        this.name = name;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public long getPort() {
        return port;
    }

    public void setPort(long port) {
        this.port = port;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
